COMMENT ON COLUMN X3DDEC_ADMIN.DEC_IWP_MATERIAL_LIST.IWP_NO IS 'IWP번호';
COMMENT ON COLUMN X3DDEC_ADMIN.DEC_IWP_MATERIAL_LIST.IWP_OID IS 'IWP ObjectId';