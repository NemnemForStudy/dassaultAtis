CREATE TABLE X3DDEC_ADMIN.DEC_IWP_MATERIAL_LIST (
	SITE_CD VARCHAR2(5) NOT NULL,
	CWP_NO VARCHAR2(30) NOT NULL,
	LINE_NO VARCHAR2(100) NOT NULL,
	ISO_NO VARCHAR2(100) NOT NULL,
	IDENT_CD VARCHAR2(20) NOT NULL,
	IWP_NO VARCHAR2(100) NOT NULL,
	IWP_OID VARCHAR2(100) NOT NULL,
	CONSTRAINT DEC_IWP_MATERIAL_LIST_PK PRIMARY KEY (SITE_CD, CWP_NO, LINE_NO, ISO_NO, IDENT_CD)
);

COMMENT ON COLUMN X3DDEC_ADMIN.DEC_IWP_MATERIAL_LIST.SITE_CD IS '현장코드';
