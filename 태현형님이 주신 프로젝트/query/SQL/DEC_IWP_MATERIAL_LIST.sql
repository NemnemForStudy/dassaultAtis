COMMENT ON COLUMN X3DDEC_ADMIN.DEC_IWP_MATERIAL_LIST.LINE_NO IS '지역명';
COMMENT ON COLUMN X3DDEC_ADMIN.DEC_IWP_MATERIAL_LIST.ISO_NO IS 'LINE번호';
